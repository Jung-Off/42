int	ft_is_prime(int nb)
{
	int n;

	if (nb <= 1)
		return (0);
	else if (nb == 2)
		return (1);
	n = 2;
	while (n <= nb / n)
	{
		if (nb % n == 0)
			return (0);
		++n;
	}
	return (1);
}
