#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t j;

	j = ft_strlen(s);
	while (s[j] != c && j > 0)
		--j;
	if (s[j] != c)
		return (0);
	else
		return ((char*)s + j);
}
